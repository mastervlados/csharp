void PrintImage(int[,] image)
{
	for (int i = 0; i < image.GetLength(0); i++) // rows
	{
		for (int j = 0; j < image.GetLength(1); j++) // columns
		{
			if (image[i, j] == 0)
			{
				Console.Write(" ");
			}
			else
			{
				Console.Write("+");
			}
		}
		Console.WriteLine();
	}
}

void FillImage(int point, int pixel) // row & col
{
	if (pic[point, pixel] == 0)
	{
		// ***********************************
		// !!! Algorithm for small images ****
		// ***********************************
		pic[point, pixel] = 1;
		FillImage(point - 1, pixel); // row - up
		FillImage(point, pixel - 1); // row - left
		FillImage(point + 1, pixel); // row - down
        FillImage(point, pixel + 1); // row - right
	}
}

